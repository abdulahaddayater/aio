export const getRecipeById = (id: string) => recipes.find((r) => r.id === id);

export const getFeaturedRecipes = () => recipes.filter((r) => r.isFeatured);

export const getTrendingRecipes = () => recipes.filter((r) => r.isTrending);

export const getSimilarRecipes = (recipe: Recipe, limit = 4) =>
  recipes
    .filter(
      (r) =>
        r.id !== recipe.id &&
        (r.cuisine === recipe.cuisine ||
          r.dietary.some((d) => recipe.dietary.includes(d)) ||
          r.mealType.some((m) => recipe.mealType.includes(m)))
    )
    .slice(0, limit);

/** Protein (g per serving) at or above which a recipe counts as "High Protein". */
export const HIGH_PROTEIN_THRESHOLD = 20;
/** Carbs (g per serving) at or below which a recipe counts as "Low Carb". */
export const LOW_CARB_THRESHOLD = 25;

/** A recipe is high protein if it carries the tag OR meets the protein threshold. */
export const isHighProtein = (recipe: Recipe) =>
  recipe.dietary.includes("High Protein") || recipe.nutrition.protein >= HIGH_PROTEIN_THRESHOLD;

/** A recipe is low carb if it carries the tag OR meets the carb threshold. */
export const isLowCarb = (recipe: Recipe) =>
  recipe.dietary.includes("Low Carb") || recipe.nutrition.carbs <= LOW_CARB_THRESHOLD;

/** Returns true when a recipe satisfies a single dietary selection. */
const matchesDietary = (recipe: Recipe, dietary: string) => {
  if (dietary === "High Protein") return isHighProtein(recipe);
  if (dietary === "Low Carb") return isLowCarb(recipe);
  return recipe.dietary.includes(dietary as never);
};

export const searchRecipes = (filters: {
  query?: string;
  cuisine?: string[];
  dietary?: string[];
  religion?: string[];
  mealType?: string[];
  cookingTime?: string | null;
  calorieRange?: string | null;
}) => {
  return recipes.filter((recipe) => {
    const q = (filters.query || "").trim().toLowerCase();
    if (q) {
      // Build a searchable text blob from every relevant field.
      const haystack = [
        recipe.name,
        recipe.cuisine,
        recipe.description,
        recipe.difficulty,
        ...recipe.tags,
        ...recipe.dietary,
        ...recipe.mealType,
        ...recipe.religion,
        ...recipe.ingredients.map((i) => i.name),
      ]
        .join(" ")
        .toLowerCase();

      // Match singular/plural for common terms (e.g. "dessert" → "desserts").
      const normalized = haystack + " " + haystack.replace(/s\b/g, "");
      // Every word in the query must appear, so "lava cake" matches "Chocolate Lava Cake".
      const words = q.split(/\s+/).filter(Boolean);
      const allWordsMatch = words.every(
        (w) => normalized.includes(w) || normalized.includes(w.replace(/s$/, ""))
      );
      if (!allWordsMatch) return false;
    }

    if (filters.cuisine?.length && !filters.cuisine.includes(recipe.cuisine)) return false;

    if (filters.dietary?.length &&
        !filters.dietary.some((d) => matchesDietary(recipe, d))) return false;

    if (filters.religion?.length && filters.religion.length > 0 &&
        !filters.religion.some((r) => recipe.religion.includes(r as never))) return false;
    if (filters.mealType?.length && !filters.mealType.some((m) => recipe.mealType.includes(m as never))) return false;

    if (filters.cookingTime) {
      if (filters.cookingTime === "Under 15 min" && recipe.cookingTime >= 15) return false;
      if (filters.cookingTime === "Under 30 min" && recipe.cookingTime >= 30) return false;
      if (filters.cookingTime === "Under 60 min" && recipe.cookingTime >= 60) return false;
    }
    if (filters.calorieRange) {
      if (filters.calorieRange === "Low Calorie" && recipe.calories > 300) return false;
      if (filters.calorieRange === "Medium Calorie" && (recipe.calories <= 300 || recipe.calories > 600)) return false;
      if (filters.calorieRange === "High Calorie" && recipe.calories <= 600) return false;
    }
    return true;
  });
};
